/*
 * noise.js — генератор «шума» для процедурного рельефа.
 *
 * Зачем это нужно:
 *   Чтобы местность выглядела естественно (плавные холмы, а не случайные точки),
 *   используют так называемый «шум». Это гладкая псевдослучайная функция:
 *   близкие точки имеют близкие значения, далёкие — разные.
 *
 * Здесь реализован «value noise» (шум по значениям) с детерминированным сидом:
 *   один и тот же сид всегда даёт одинаковую карту.
 */

class Noise {
  /** @param {number} seed — зерно генерации (целое число) */
  constructor(seed) {
    this.seed = seed >>> 0; // приводим к беззнаковому 32-битному целому
  }

  /*
   * Псевдослучайное число в диапазоне [0, 1) для целочисленных координат (ix, iy).
   * Детерминированно: одинаковые входы → одинаковый результат.
   * Это «хеш-функция» — перемешивает биты, чтобы получить как-бы-случайное число.
   */
  _hash(ix, iy) {
    let h = this.seed + ix * 374761393 + iy * 668265263;
    h = (h ^ (h >> 13)) * 1274126177;
    h = h ^ (h >> 16);
    // h может быть отрицательным — берём по модулю и нормируем в [0, 1)
    return (h >>> 0) / 4294967295;
  }

  /*
   * Плавная интерполяция (smoothstep) — сглаживает переход между значениями,
   * чтобы рельеф не имел резких «ступенек».
   */
  _smooth(t) {
    return t * t * (3 - 2 * t);
  }

  /*
   * Значение шума в произвольной (дробной) точке (x, y).
   * Берём 4 ближайших целых угла, считаем для них хеш и плавно смешиваем.
   * Результат в диапазоне [0, 1).
   */
  value(x, y) {
    const ix = Math.floor(x);
    const iy = Math.floor(y);
    const fx = x - ix; // дробная часть по X
    const fy = y - iy; // дробная часть по Y

    // значения в четырёх углах клетки
    const v00 = this._hash(ix, iy);
    const v10 = this._hash(ix + 1, iy);
    const v01 = this._hash(ix, iy + 1);
    const v11 = this._hash(ix + 1, iy + 1);

    // плавные веса
    const sx = this._smooth(fx);
    const sy = this._smooth(fy);

    // смешиваем сначала по горизонтали, потом по вертикали (билинейно)
    const top = v00 + (v10 - v00) * sx;
    const bottom = v01 + (v11 - v01) * sx;
    return top + (bottom - top) * sy;
  }

  /*
   * Фрактальный шум (fBm — fractional Brownian motion).
   * Складываем несколько «слоёв» шума разной частоты:
   *   - крупные формы (горы, долины) дают низкие частоты,
   *   - мелкие детали (бугорки) дают высокие частоты.
   * octaves   — сколько слоёв складываем (больше = детальнее)
   * frequency — начальная частота (масштаб крупных форм)
   * Результат нормирован в [0, 1].
   */
  fractal(x, y, octaves = 5, frequency = 1) {
    let total = 0;
    let amplitude = 1;
    let freq = frequency;
    let maxValue = 0; // для нормализации результата

    for (let i = 0; i < octaves; i++) {
      total += this.value(x * freq, y * freq) * amplitude;
      maxValue += amplitude;
      amplitude *= 0.5; // каждый следующий слой в 2 раза слабее
      freq *= 2;        // и в 2 раза «мельче»
    }

    return total / maxValue;
  }
}
